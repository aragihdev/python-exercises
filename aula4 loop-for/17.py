print("Verificador de paridade personalizado")

num = int(input("Digite um número qualquer:"))

if num % 2 == 0:
    print(f"O número {num} é par e se divido por 2 virará {num / 2}.")
else:
    print(f"O número {num} é ímpar e se multiplicado por 3 virará {num * 3}.")

